public class PrimitiveTypes {

    public static void main(String[] args)
    {
        int b = 130;
        short shortvalue = 200;
        long longvalue = 45_00_000;
        char charValue = 'd';
        int a = 2000;

        boolean flag = true;

        float x = 45.45f;
        double d = 3464356.78;

        byte bytevalue = (byte) b;

        System.out.println("Int value is : "+b);
        System.out.println("Short value is : "+shortvalue);
        System.out.println("Long value is : "+longvalue);
        System.out.println("Char value is : "+charValue);
        System.out.println("Integer value is : "+a);
        System.out.println("Boolean value is : "+flag);
        System.out.println("Float value is : "+x);
        System.out.println("Double value is : "+d);
        System.out.println("Byte value is : "+bytevalue);
    }
}